package com.example.dell.a19febnoon;

public class ContactObject {

    String namee;
    String phoneNumber;

    public ContactObject(String namee, String phoneNumber) {
        this.namee = namee;
        this.phoneNumber = phoneNumber;

    }

    public String getNamee() {
        return namee;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }
}
